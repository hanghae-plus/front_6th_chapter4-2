import {
  Box,
  Button,
  Flex,
  Grid,
  GridItem,
  Popover,
  PopoverArrow,
  PopoverBody,
  PopoverCloseButton,
  PopoverContent,
  PopoverTrigger,
  Text,
} from "@chakra-ui/react";
import { CellSize, DAY_LABELS, 분 } from "./constants.ts";
import { Schedule } from "./types.ts";
import { fill2, parseHnM } from "./utils.ts";
import { useDndContext, useDraggable } from "@dnd-kit/core";
import { CSS } from "@dnd-kit/utilities";
import React, { Fragment, useCallback, useMemo } from "react";
import { useSchedulesDispatch } from "./ScheduleContext.tsx";

interface Props {
  tableId: string;
  schedules: Schedule[];
  openSearch: (
    tableId: string,
    timeInfo?: { day: string; time: number }
  ) => void;
}

const TIMES = [
  ...Array(18)
    .fill(0)
    .map((v, k) => v + k * 30 * 분)
    .map((v) => `${parseHnM(v)}~${parseHnM(v + 30 * 분)}`),

  ...Array(6)
    .fill(18 * 30 * 분)
    .map((v, k) => v + k * 55 * 분)
    .map((v) => `${parseHnM(v)}~${parseHnM(v + 50 * 분)}`),
] as const;

const ScheduleTable = ({ tableId, schedules, openSearch }: Props) => {
  const setSchedulesMap = useSchedulesDispatch();

  const lectureIds = useMemo(
    () => [...new Set(schedules.map(({ lecture }) => lecture.id))],
    [schedules]
  );
  const colors = ["#fdd", "#ffd", "#dff", "#ddf", "#fdf", "#dfd"] as const;
  const getColor = (lectureId: string): string =>
    colors[lectureIds.indexOf(lectureId) % colors.length];

  const handleScheduleTimeClick = useCallback(
    (day: string, time: number) => {
      openSearch(tableId, { day, time });
    },
    [openSearch, tableId]
  );

  const handleDeleteButtonClick = useCallback(
    (day: string, time: number) => {
      setSchedulesMap((prev) => ({
        ...prev,
        [tableId]: prev[tableId].filter(
          (schedule) => schedule.day !== day || !schedule.range.includes(time)
        ),
      }));
    },
    [setSchedulesMap, tableId]
  );

  return (
    <Box position="relative">
      <ActiveOutlineOverlay tableId={tableId} />
      <TimeGrid onCellClick={handleScheduleTimeClick} />
      {schedules.map((schedule, index) => (
        <DraggableSchedule
          key={`${schedule.lecture.title}-${index}`}
          id={`${tableId}:${index}`}
          data={schedule}
          bg={getColor(schedule.lecture.id)}
          onDelete={handleDeleteButtonClick}
        />
      ))}
    </Box>
  );
};

const DraggableSchedule = React.memo(
  ({
    id,
    data,
    bg,
    onDelete,
  }: {
    id: string;
    data: Schedule;
    bg: string;
    onDelete: (day: string, time: number) => void;
  }) => {
    const { day, range, room, lecture } = data;
    const { attributes, setNodeRef, listeners, transform } = useDraggable({
      id,
    });
    const leftIndex = DAY_LABELS.indexOf(day as (typeof DAY_LABELS)[number]);
    const topIndex = range[0] - 1;
    const size = range.length;

    return (
      <Popover isLazy lazyBehavior="unmount">
        <PopoverTrigger>
          <Box
            position="absolute"
            left={`${120 + CellSize.WIDTH * leftIndex + 1}px`}
            top={`${40 + (topIndex * CellSize.HEIGHT + 1)}px`}
            width={CellSize.WIDTH - 1 + "px"}
            height={CellSize.HEIGHT * size - 1 + "px"}
            bg={bg}
            p={1}
            boxSizing="border-box"
            cursor="pointer"
            ref={setNodeRef}
            transform={CSS.Translate.toString(transform)}
            {...listeners}
            {...attributes}
          >
            <Text fontSize="sm" fontWeight="bold">
              {lecture.title}
            </Text>
            <Text fontSize="xs">{room}</Text>
          </Box>
        </PopoverTrigger>
        <PopoverContent onClick={(event) => event.stopPropagation()}>
          <PopoverArrow />
          <PopoverCloseButton />
          <PopoverBody>
            <Text>강의를 삭제하시겠습니까?</Text>
            <Button
              colorScheme="red"
              size="xs"
              onClick={() => onDelete(day, range[0])}
            >
              삭제
            </Button>
          </PopoverBody>
        </PopoverContent>
      </Popover>
    );
  }
);

export default React.memo(ScheduleTable);

// '이 테이블에 Dnd를 활성화 할거야'를 나타내는 오버레이를 분리하여 전체 리렌더 방지
const ActiveOutlineOverlay = ({ tableId }: { tableId: string }) => {
  const dndContext = useDndContext();
  const activeId = dndContext.active?.id;
  const activeTableId = activeId ? String(activeId).split(":")[0] : null;
  if (activeTableId !== tableId) return null;

  return (
    <Box
      pointerEvents="none"
      position="absolute"
      top={0}
      right={0}
      bottom={0}
      left={0}
      outline="5px dashed"
      outlineColor="blue.300"
      borderRadius="md"
    />
  );
};

// 정적 격자(헤더/타임셀)를 분리하여 재사용 및 리렌더 스킵
const TimeGrid = React.memo(
  ({ onCellClick }: { onCellClick: (day: string, time: number) => void }) => {
    return (
      <Grid
        templateColumns={`120px repeat(${DAY_LABELS.length}, ${CellSize.WIDTH}px)`}
        templateRows={`40px repeat(${TIMES.length}, ${CellSize.HEIGHT}px)`}
        bg="white"
        fontSize="sm"
        textAlign="center"
        outline="1px solid"
        outlineColor="gray.300"
      >
        <GridItem key="교시" borderColor="gray.300" bg="gray.100">
          <Flex justifyContent="center" alignItems="center" h="full" w="full">
            <Text fontWeight="bold">교시</Text>
          </Flex>
        </GridItem>
        {DAY_LABELS.map((day) => (
          <GridItem
            key={day}
            borderLeft="1px"
            borderColor="gray.300"
            bg="gray.100"
          >
            <Flex justifyContent="center" alignItems="center" h="full">
              <Text fontWeight="bold">{day}</Text>
            </Flex>
          </GridItem>
        ))}
        {TIMES.map((time, timeIndex) => (
          <Fragment key={`시간-${timeIndex + 1}`}>
            <GridItem
              borderTop="1px solid"
              borderColor="gray.300"
              bg={timeIndex > 17 ? "gray.200" : "gray.100"}
            >
              <Flex justifyContent="center" alignItems="center" h="full">
                <Text fontSize="xs">
                  {fill2(timeIndex + 1)} ({time})
                </Text>
              </Flex>
            </GridItem>
            {DAY_LABELS.map((day) => (
              <GridItem
                key={`${day}-${timeIndex + 2}`}
                borderWidth="1px 0 0 1px"
                borderColor="gray.300"
                bg={timeIndex > 17 ? "gray.100" : "white"}
                cursor="pointer"
                _hover={{ bg: "yellow.100" }}
                onClick={() => onCellClick(day, timeIndex + 1)}
              />
            ))}
          </Fragment>
        ))}
      </Grid>
    );
  }
);
